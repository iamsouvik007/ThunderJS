// Variation of TC-1: Even number
let num = 8;
if (num % 2 === 0) {
    console.log(num + " is Even");
} else {
    console.log(num + " is Odd");
}

// Variation: negative number
let neg = -3;
if (neg % 2 === 0) {
    console.log(neg + " is Even");
} else {
    console.log(neg + " is Odd");
}

// Variation: zero
let zero = 0;
if (zero % 2 === 0) {
    console.log(zero + " is Even");
} else {
    console.log(zero + " is Odd");
}
